package net.aveunsoft;

public class DataTypes {
    public static void main(String[] args) {

        //integers
        byte integer1 = 1;
        short integer2 = 31;
        int integer3 = 435;
        long integer4 = 432423;

        //decimals
        float decimal1 = 43.2f;
        double decimal2 = 342.324d;

        //only accepts one character
        char character1 = 'a';

        //booleans
        boolean boolean1 = true;
        boolean boolean2 = false;

        //strings
        String name = "David";
        String last_name = "Guerra";
    }
}
